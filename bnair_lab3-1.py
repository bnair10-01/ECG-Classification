class W2VA(nn.Module):
    """Wav2Vec2 with Adapter-style classifier head (frozen backbone)"""
    def __init__(self, enc, h=256, pd=0.1):
        super().__init__()
        self.enc = enc
        hs = self.enc.config.hidden_size
        self.head = nn.Sequential(
            nn.Linear(hs, h),
            nn.ReLU(),
            nn.Dropout(pd),
            nn.Linear(h, 1)
        )

    def forward(self, input_values, attention_mask=None, labels=None):
        out = self.enc(input_values=input_values, attention_mask=attention_mask, return_dict=True)
        hid = out.last_hidden_state

        # Use downsampled mask if available
        fm = getattr(out, "extractor_attention_mask", None)
        if fm is None and attention_mask is not None:
            T = hid.size(1)
            fm = F.interpolate(attention_mask[:, None, :].float(), size=T, mode="nearest").squeeze(1)

        # Mask-aware pooling
        if fm is not None:
            msk = fm.unsqueeze(-1).type_as(hid)
            pool = (hid * msk).sum(dim=1) / msk.sum(dim=1).clamp_min(1e-6)
        else:
            pool = hid.mean(dim=1)

        lg = self.head(pool)

        if labels is not None:
            l = labels.unsqueeze(1).to(lg.dtype)
            loss = F.binary_cross_entropy_with_logits(lg, l)
            return type("O", (), {"loss": loss, "logits": lg})
        return type("O", (), {"logits": lg})

