def plot_cm(cm, cn=("Negative", "Positive"), t="Confusion Matrix"):
    """Plot confusion matrix with counts and percentages"""
    cm = np.array(cm, dtype=np.float64)
    rs = cm.sum(axis=1, keepdims=True)
    rs[rs == 0] = 1.0
    cp = cm / rs

    fig, ax = plt.subplots(figsize=(6, 5))
    im = ax.imshow(cm, interpolation='nearest', cmap='Blues')
    ax.set_title(t, fontsize=14, fontweight='bold')
    plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04)

    ax.set_xticks(np.arange(len(cn)))
    ax.set_yticks(np.arange(len(cn)))
    ax.set_xticklabels(cn)
    ax.set_yticklabels(cn)
    ax.set_ylabel("True Label", fontsize=12)
    ax.set_xlabel("Predicted Label", fontsize=12)

    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            ax.text(j, i, f"{int(cm[i, j])}\n({cp[i, j]*100:.1f}%)",
                   ha="center", va="center", fontsize=11,
                   color="white" if cm[i, j] > cm.max() / 2 else "black")

    fig.tight_layout()
    save_fig(fig, t)
    plt.close()

