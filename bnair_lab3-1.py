def zs(x):
    """Per-row z-score normalization for batches"""
    m = x.mean(dim=1, keepdim=True)
    s = x.std(dim=1, unbiased=False, keepdim=True).clamp_min(1e-6)
    return (x - m) / s

def up(x1d, tl=TL):
    """Upsample 1D signal using linear interpolation"""
    x = x1d.view(1, 1, -1)
    y = F.interpolate(x, size=tl, mode="linear", align_corners=False)
    return y.view(-1)

def to_inp(sigs, labs, proc):
    """
    Convert ECG signals to audio model inputs

    Args:
        sigs: List of ECG signal arrays
        labs: List of labels
        proc: HuggingFace processor (handles padding, attention masks)

    Returns:
        Dict with input_values, attention_mask, and labels
    """
    B = len(sigs)
    x = torch.stack([torch.as_tensor(s, dtype=torch.float32) for s in sigs], dim=0)
    x = zs(x)  # Normalize
    x = torch.stack([up(r) for r in x], dim=0)  # Resample
    p = proc(x.numpy(), sampling_rate=16000, return_tensors="pt", padding=True)
    return {
        "input_values": p["input_values"],
        "attention_mask": p.get("attention_mask", None),
        "labels": torch.as_tensor(labs, dtype=torch.float32)
    }

# Metrics & Evaluation

def comp_met(yt, yp):
    """
    Compute classification metrics

    Args:
        yt: True labels
        yp: Predicted probabilities

    Returns:
        Dict with acc, precision, recall, f1, auc, confusion matrix
    """
    yt = np.asarray(yt).astype(int)
    yp = np.asarray(yp).astype(float)
    pred = (yp >= 0.5).astype(int)

    acc = accuracy_score(yt, pred)
    prec, rec, f1, _ = precision_recall_fscore_support(yt, pred, average="binary", zero_division=0)

    try:
        auc = roc_auc_score(yt, yp) if len(np.unique(yt)) > 1 else float("nan")
    except:
        auc = float("nan")

    cm = confusion_matrix(yt, pred)

    return {"acc": acc, "precision": prec, "recall": rec, "f1": f1, "auc": auc, "cm": cm}

def mv(x, d):
    """Recursively move tensors/dicts/lists to device"""
    if isinstance(x, torch.Tensor):
        return x.to(d)
    if isinstance(x, dict):
        return {k: mv(v, d) for k, v in x.items() if v is not None}
    if isinstance(x, (list, tuple)):
        return type(x)(mv(v, d) for v in x)
    return x

def b2il(b, d):
    """Convert batch to inputs dict and labels tensor"""
    if isinstance(b, dict):
        l = b["labels"]
        inp = {k: v for k, v in b.items() if k != "labels"}
        return mv(inp, d), l.to(d).float()

    if isinstance(b, (list, tuple)) and len(b) == 2:
        a, c = b
        if isinstance(a, dict):
            return mv(a, d), c.to(d).float()
        else:
            return mv({"input_values": a}, d), c.to(d).float()

    raise ValueError("Unexpected batch format")

# Training & Evaluation
def train(mdl, proc, tr_ld, vl_ld, d, lr=5e-5, ep=5, wd=1e-4, pat=2, verbose=True):
    """
    Train model with early stopping and metric tracking

    Args:
        mdl: PyTorch model
        proc: Preprocessor/processor
        tr_ld: Train dataloader
        vl_ld: Validation dataloader
        d: Device
        lr: Learning rate
        ep: Max epochs
        wd: Weight decay
        pat: Early stopping patience
        verbose: Print training progress

    Returns:
        Trained model and history dict
    """
    mdl = mdl.to(d)
    opt = AdamW((p for p in mdl.parameters() if p.requires_grad), lr=lr, weight_decay=wd)
    bv, bs, ni = float("inf"), None, 0

    # Track metrics per epoch
    history = {
        'epoch': [],
        'train_loss': [],
        'val_loss': [],
        'val_acc': [],
        'val_precision': [],
        'val_recall': [],
        'val_f1': [],
        'val_auc': []
    }

    if verbose:
        print(f"\nTraining: lr={lr}, epochs={ep}, wd={wd}, patience={pat}")

    for e in range(1, ep + 1):
        # Training phase
        mdl.train()
        rl = 0.0
        for bi, b in enumerate(tr_ld):
            inp, lab = b2il(b, d)
            opt.zero_grad(set_to_none=True)
            out = mdl(**inp, labels=lab)
            out.loss.backward()
            opt.step()
            rl += out.loss.item()

            if verbose and (bi + 1) % 10 == 0:
                print(f"  Epoch {e}/{ep} Batch {bi+1}/{len(tr_ld)} Loss: {out.loss.item():.4f}")

        tl = rl / max(1, len(tr_ld))

        # Validation phase
        mdl.eval()
        vl = 0.0
        with torch.no_grad():
            for b in vl_ld:
                inp, lab = b2il(b, d)
                out = mdl(**inp, labels=lab)
                vl += out.loss.item()
        vl /= max(1, len(vl_ld))

        # Compute validation metrics
        vm = eval_mdl(mdl, proc, vl_ld, d)

        # Store history
        history['epoch'].append(e)
        history['train_loss'].append(tl)
        history['val_loss'].append(vl)
        history['val_acc'].append(vm['acc'])
        history['val_precision'].append(vm['precision'])
        history['val_recall'].append(vm['recall'])
        history['val_f1'].append(vm['f1'])
        history['val_auc'].append(vm['auc'])

        if verbose:
            print(f"  Epoch {e}/{ep} TrLoss: {tl:.4f} VaLoss: {vl:.4f} VaAcc: {vm['acc']:.4f} VaF1: {vm['f1']:.4f}")

        # Early stopping
        if vl < bv - 1e-6:
            bv = vl
            bs = deepcopy({k: v.detach().cpu() for k, v in mdl.state_dict().items()})
            ni = 0
            if verbose:
                print(f"  Best validation loss: {bv:.4f}")
        else:
            ni += 1
            if verbose:
                print(f"  No improvement for {ni} epoch(s)")
            if ni >= pat:
                if verbose:
                    print("  Early stopping triggered")
                break

    # Restore best model
    if bs is not None:
        mdl.load_state_dict(bs)
        mdl.to(d)
        if verbose:
            print(f"Loaded best model (val_loss={bv:.4f})")

    return mdl, history

def eval_mdl(mdl, proc, ld, d):
    """Evaluate model and return metrics"""
    mdl.eval()
    ap, al = [], []

    with torch.no_grad():
        for b in ld:
            inp, lab = b2il(b, d)
            out = mdl(**inp)
            lg = out.logits if hasattr(out, "logits") else out
            pr = torch.sigmoid(lg.squeeze(-1))
            ap.append(pr.detach().cpu())
            al.append(lab.detach().cpu())

    yp = torch.cat(ap, dim=0).numpy()
    yt = torch.cat(al, dim=0).numpy().astype(int)

    return comp_met(yt, yp)


