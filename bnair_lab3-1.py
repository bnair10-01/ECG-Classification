class LRL(nn.Module):
    """LoRA Linear layer wrapper"""
    def __init__(self, bl, r=8, a=16, d=0.05):
        super().__init__()
        self.base = bl
        for p in self.base.parameters():
            p.requires_grad = False

        self.r, self.scale = r, a / float(r)
        self.lora_A = nn.Linear(self.base.in_features, r, bias=False)
        self.lora_B = nn.Linear(r, self.base.out_features, bias=False)
        self.drop = nn.Dropout(d)

        # Standard LoRA initialization
        nn.init.kaiming_uniform_(self.lora_A.weight, a=math.sqrt(5))
        nn.init.zeros_(self.lora_B.weight)

    def forward(self, x):
        return self.base(x) + self.drop(self.lora_B(self.lora_A(x))) * self.scale

def apply_lora(mod, r=8, a=16, d=0.05):
    """Recursively inject LoRA into all Linear layers"""
    for n, c in list(mod.named_children()):
        if isinstance(c, nn.Linear):
            setattr(mod, n, LRL(c, r=r, a=a, d=d))
        else:
            apply_lora(c, r=r, a=a, d=d)

class ECL(nn.Module):
    """ECG-FM with LoRA fine-tuning"""
    def __init__(self, bb, h=256, pd=0.1, r=8, a=16, ld=0.05):
        super().__init__()
        self.enc = bb
        for p in self.enc.parameters():
            p.requires_grad = False

        # Inject LoRA
        apply_lora(self.enc, r=r, a=a, d=ld)

        # Infer hidden dimension
        hd = getattr(self.enc, "config", types.SimpleNamespace(hidden_size=None)).hidden_size
        if hd is None:
            with torch.no_grad():
                dm = torch.zeros(2, 1000)
                out = self.enc(dm)
                hd = out.last_hidden_state.size(-1)

        self.head = nn.Sequential(
            nn.Linear(hd, h),
            nn.ReLU(),
            nn.Dropout(pd),
            nn.Linear(h, 1)
        )

    def forward(self, input_values, attention_mask=None, labels=None):
        out = self.enc(input_values)
        hid = out.last_hidden_state

        fm = getattr(out, "extractor_attention_mask", None)
        if fm is None and attention_mask is not None:
            T = hid.size(1)
            fm = F.interpolate(attention_mask[:, None, :].float(), size=T, mode="nearest").squeeze(1)

        if fm is not None:
            msk = fm.unsqueeze(-1).type_as(hid)
            pool = (hid * msk).sum(dim=1) / msk.sum(dim=1).clamp_min(1e-6)
        else:
            pool = hid.mean(dim=1)

        lg = self.head(pool)

        if labels is not None:
            l = labels.unsqueeze(1).to(lg.dtype)
            loss = F.binary_cross_entropy_with_logits(lg, l)
            return types.SimpleNamespace(loss=loss, logits=lg)
        return types.SimpleNamespace(logits=lg)

