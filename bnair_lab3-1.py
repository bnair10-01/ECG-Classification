TL = 1000  

def interp(x, tl):
    """Linear interpolation from original length to target length"""
    sl = x.shape[0]
    if sl == tl:
        return x
    xp = np.linspace(0, 1, num=sl, dtype=np.float32)
    xn = np.linspace(0, 1, num=tl, dtype=np.float32)
    return np.interp(xn, xp, x.astype(np.float32)).astype(np.float32)

class DS(Dataset):
    """ECG Dataset with automatic resampling to target length"""
    def __init__(self, X, y, tl=TL):
        self.X, self.y, self.tl = X, y, tl

    def __len__(self):
        return len(self.X)

    def __getitem__(self, i):
        wave = interp(self.X[i], self.tl)
        return torch.from_numpy(wave), torch.tensor(self.y[i], dtype=torch.long)

