class ECGB(nn.Module):
    """Simple CNN backbone for ECG-FM (fallback if checkpoint unavailable)"""
    def __init__(self, ic=1, h=128):
        super().__init__()
        self.feat = nn.Sequential(
            nn.Conv1d(ic, 64, 7, stride=2, padding=3), nn.ReLU(),
            nn.Conv1d(64, 128, 7, stride=2, padding=3), nn.ReLU(),
            nn.Conv1d(128, h, 7, stride=2, padding=3), nn.ReLU()
        )
        self.config = types.SimpleNamespace(hidden_size=h)

    def forward(self, x):
        if x.dim() == 2:
            x = x.unsqueeze(1)
        h = self.feat(x)
        h = h.transpose(1, 2).contiguous()
        return types.SimpleNamespace(last_hidden_state=h, extractor_attention_mask=None)

